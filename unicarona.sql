DROP TABLE IF EXISTS VEICULO CASCADE;
DROP TABLE IF EXISTS MOTORISTA CASCADE;
DROP TABLE IF EXISTS USUARIO CASCADE;

-- CRIANDO TABELA USUARIO
CREATE TABLE USUARIO (
    cpf VARCHAR(11) NOT NULL,
    nome VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    telefone VARCHAR(15),
    CONSTRAINT PK_USUARIO PRIMARY KEY (cpf)
);

-- CRIANDO TABELA MOTORISTA
CREATE TABLE MOTORISTA (
    cpf VARCHAR(11) NOT NULL,
    numero_cnh VARCHAR(20) NOT NULL UNIQUE,
    validade_cnh DATE NOT NULL,
    CONSTRAINT PK_MOTORISTA PRIMARY KEY (cpf),
    CONSTRAINT FK_MOTORISTA_USUARIO FOREIGN KEY (cpf) REFERENCES USUARIO(cpf) ON DELETE CASCADE
);

-- CRIANDO TABELA VEICULO
CREATE TABLE VEICULO (
    placa VARCHAR(7) NOT NULL,
    cpf_motorista VARCHAR(11) NOT NULL,
    modelo VARCHAR(50) NOT NULL,
    marca VARCHAR(50) NOT NULL,
    cor VARCHAR(30),
    capacidade_passageiros INT NOT NULL,
    CONSTRAINT PK_VEICULO PRIMARY KEY (placa),
    CONSTRAINT FK_VEICULO_MOTORISTA FOREIGN KEY (cpf_motorista) REFERENCES MOTORISTA(cpf) ON DELETE CASCADE,
    CONSTRAINT UNQ_VEICULO_MOTORISTA UNIQUE (placa, cpf_motorista)
);

-- CRIANDO TABELA LOCAL
CREATE TABLE LOCAL (
    id_local INT NOT NULL,
    nome_local VARCHAR(100) NOT NULL,
    cidade VARCHAR(50) NOT NULL,
    bairro VARCHAR(50),
    CONSTRAINT PK_LOCAL PRIMARY KEY (id_local)
);

-- CRIANDO TABELA CARONA
CREATE TABLE CARONA (
    id_carona INT NOT NULL,
    cpf_motorista VARCHAR(11) NOT NULL,
    placa_veiculo VARCHAR(7) NOT NULL,
    id_origem INT NOT NULL,
    id_destino INT NOT NULL,
    data DATE NOT NULL,
    horario TIME NOT NULL,
    vagas_disponiveis INT NOT NULL,
    CONSTRAINT PK_CARONA PRIMARY KEY (id_carona),
    CONSTRAINT FK_CARONA_VEICULO FOREIGN KEY (placa_veiculo) REFERENCES VEICULO(placa),
    CONSTRAINT FK_CARONA_MOTORISTA FOREIGN KEY (cpf_motorista) REFERENCES MOTORISTA(cpf),
    CONSTRAINT FK_CARONA_ORIGEM FOREIGN KEY (id_origem) REFERENCES LOCAL(id_local),
    CONSTRAINT FK_CARONA_DESTINO FOREIGN KEY (id_destino) REFERENCES LOCAL(id_local)
);

-- CRIANDO TABELA RESERVA
CREATE TABLE RESERVA (
    id_reserva INT NOT NULL,
    cpf_usuario VARCHAR(11) NOT NULL,
    id_carona INT NOT NULL,
    status VARCHAR(20) NOT NULL,
    data_reserva DATE NOT NULL,
    CONSTRAINT PK_RESERVA PRIMARY KEY (id_reserva),
    CONSTRAINT FK_RESERVA_USUARIO FOREIGN KEY (cpf_usuario) REFERENCES USUARIO(cpf),
    CONSTRAINT FK_RESERVA_CARONA FOREIGN KEY (id_carona) REFERENCES CARONA(id_carona)
);

-- CRIANDO TABELA MENSAGEM
CREATE TABLE MENSAGEM (
    id_mensagem INT NOT NULL,
    cpf_remetente VARCHAR(11) NOT NULL,
    cpf_destinatario VARCHAR(11) NOT NULL,
    conteudo TEXT NOT NULL,
    data_envio DATE NOT NULL,
    CONSTRAINT PK_MENSAGEM PRIMARY KEY (id_mensagem),
    CONSTRAINT FK_MENSAGEM_REMETENTE FOREIGN KEY (cpf_remetente) REFERENCES USUARIO(cpf),
    CONSTRAINT FK_MENSAGEM_DESTINATARIO FOREIGN KEY (cpf_destinatario) REFERENCES USUARIO(cpf)
);

-- CRIANDO TABELA AVALIACAO
CREATE TABLE AVALIACAO (
    id_avaliacao INT NOT NULL,
    cpf_avaliador VARCHAR(11) NOT NULL,
    cpf_avaliado VARCHAR(11) NOT NULL,
    nota INT NOT NULL CHECK (nota BETWEEN 1 AND 5),
    comentario TEXT,
    data DATE NOT NULL,
    CONSTRAINT PK_AVALIACAO PRIMARY KEY (id_avaliacao),
    CONSTRAINT FK_AVALIACAO_AVALIADOR FOREIGN KEY (cpf_avaliador) REFERENCES USUARIO(cpf),
    CONSTRAINT FK_AVALIACAO_AVALIADO FOREIGN KEY (cpf_avaliado) REFERENCES USUARIO(cpf)
);

-- CRIANDO TABELA PAGAMENTO
CREATE TABLE PAGAMENTO (
    id_pagamento INT NOT NULL,
    id_reserva INT NOT NULL,
    valor NUMERIC(10,2) NOT NULL,
    status VARCHAR(20) NOT NULL,
    forma_pagamento VARCHAR(30) NOT NULL,
    data_pagamento DATE,
    CONSTRAINT PK_PAGAMENTO PRIMARY KEY (id_pagamento),
    CONSTRAINT FK_PAGAMENTO_RESERVA FOREIGN KEY (id_reserva) REFERENCES RESERVA(id_reserva)
);

