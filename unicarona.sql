-- CONSULTA 1: Relatório de Segurança - Monitoramento de CNHs Expirando
-- Esta consulta realiza uma junção interna (JOIN) entre as tabelas MOTORISTA e USUARIO para projetar os dados cadastrais e de contato de condutores específicos.
-- O filtro condicional (WHERE) atua restringindo o resultado a um intervalo temporal de três anos, permitindo que a administração da plataforma identifique 
-- quais motoristas possuem habilitações vencidas ou com vencimento próximo para fins de auditoria de segurança.
